/*Conditional statements in JavaScript allow you to execute specific blocks of code based on conditions. 
If the condition meets then a particular block of action will be executed otherwise 
it will execute another block of action that satisfies that particular condition. */
/* Examples are :-
1. if Statement
2. if-else Statement
3. else if Statement
4. switch Statement
5. Ternary Operator */

let age = 25; 
  
if (age >= 18) { 
    console.log("You are eligible of driving licence") 
} else { 
    console.log("You are not eligible for driving licence") 
}